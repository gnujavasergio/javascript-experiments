// Increment
let price = 200;
// Incremento
console.log(`Pre-incremento ++price = ${++price}`);
console.log(`price = $price`);

price = 200;
console.log(`Post-incremento price++ = ${price++}`);
console.log(`price = $price`);

// Decremento
console.log(`Pre-drecremento --price = ${--price}`);
console.log(`price = $price`);

price = 200;
console.log(`Post-decremento price-- = ${price--}`);
console.log(`price = $price`);