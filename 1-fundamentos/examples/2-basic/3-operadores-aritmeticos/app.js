let price = 200;
let result = 0;

console.log("price = " + price.toString());

// Addition
result = price + 100;
console.log("price + 100 = " + result.toString());

// Subtraction
result = price - 10;
console.log("price - 10 = " + result.toString());

// Multiplication
result = price * 2;
console.log("price * 2 = " + result.toString());

// Division
result = price / 2;
console.log("price / 2 = " + result.toString());

// Exponentiation
result = price ** 2;
console.log("price ** 2 = " + result.toString());

// Modulus
result = price % 3;
console.log("price % 3 = " + result.toString());

